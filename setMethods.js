// 1. setDate()
let d = new Date();
d.setDate(20);
console.log(d);

// 2. setFullYear()
let d = new Date();
d.setFullYear(2016);
console.log(d);

// 3. setHours()
let d = new Date();
d.setHours(15);
console.log(d);

// 4. setMilliseconds()
let d = new Date();
d.setMilliseconds(548);
console.log(d);

